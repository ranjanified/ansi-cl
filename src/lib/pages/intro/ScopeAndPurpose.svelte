    Scope and Purpose
</div>
<div class="mdc-typography--body1" style="margin-top: 4px;">
  The specification set forth in this document is designed to promote the
  portability of Common Lisp programs among a variety of data processing
  systems. It is a language specification aimed at an audience of implementors
  and knowledgeable programmers. It is neither a tutorial nor an implementation
  guide.
